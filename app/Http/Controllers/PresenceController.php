    public function employeePresence (Employee $employee) {
        $presence = Presence::where('employee_id', $employee->id)->get();
        $data = [
            "name" => $employee->user->name,
            "position" => $employee->position->position_name,
            "presence" =>  $presence,

        ];
        return response()->base_response($data);
    }
